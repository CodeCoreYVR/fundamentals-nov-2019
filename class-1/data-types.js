// Strings
// "This uses double quotes"
// 'This uses single quotes'
// `This uses backticks`
"one" + "two" // "onetwo"
'4' + 5 // '45'

// We can access the String prototype with the dot notation
"abc".length // 3

// We can access characters in a string with the bracket notation
"abc"[2] // "c"

// You cannot mutate a string with bracket notation
// let s = 'bobbi'
// s[4] = 'y'
s // 'bobbi'

// parse a string to a number
parseInt("123.4554456") // 123
parseFloat("123.4554456") // 123.4554456

// Unary operator
+"2" // 2 

